# Distributive Law Program in Python

# Taking input from user
print("Enter 3 numbers:")
a = int(input("enter the value of a: "))
b = int(input("enter the value of b: "))
c = int(input("enter the value of c: "))

print("Distributive Law")
print("A * (B * C) = ", a * (b * c))
print("(A * B) * C = ", (a * b) * c)
